function cart1(){
    var name = document.getElementById('bb1').innerText;
    var priceText = document.getElementById('bbb1').innerText;
    var price = parseFloat(priceText.replace('Rs', '').trim());
    var quantity = document.getElementById('q1').value;
    var total=price*quantity;
    
    document.getElementById("name").innerText = name;
    document.getElementById("qun").innerText = quantity;
    document.getElementById("price").innerText = "RS: "+ price;
    if(quantity=="")
        {
            document.getElementById("tprice").innerText ="RS: "+ price;
        }
        else
        {
            document.getElementById("tprice").innerText ="RS: "+ total;
        }

}
